sprites.onOverlap(SpriteKind.HealerSlime, SpriteKind.Player, function (sprite, otherSprite) {
    MaxHealthCheck(mySprite)
    if (MaxHP == 0) {
        if (HealingTimer + 500 < game.runtime()) {
            HealingTimer = game.runtime()
            PlayerHealth.value += 15
            MoveCoords = [otherSprite.x + randint(-32, 32), otherSprite.y + randint(-32, 32)]
            while (Math.abs(MoveCoords[0]) < 16) {
                MoveCoords[0] = otherSprite.x + randint(-32, 32)
            }
            while (Math.abs(MoveCoords[1]) < 16) {
                MoveCoords[1] = otherSprite.y + randint(-32, 32)
            }
            spriteutils.moveTo(sprite, spriteutils.pos(MoveCoords[0], MoveCoords[1]), 250)
        }
    } else if (MaxHP == 1) {
        if (HealingTimer + 500 < game.runtime()) {
            HealingTimer = game.runtime()
            MoveCoords = [otherSprite.x + randint(-32, 32), otherSprite.y + randint(-32, 32)]
            while (Math.abs(MoveCoords[0]) < 32) {
                MoveCoords[0] = otherSprite.x + randint(-32, 32)
            }
            while (Math.abs(MoveCoords[1]) < 32) {
                MoveCoords[1] = otherSprite.y + randint(-32, 32)
                spriteutils.moveTo(sprite, spriteutils.pos(MoveCoords[0], MoveCoords[1]), 250)
            }
        }
        PlayerHealth.value += 0
    }
})
