scene.onHitTile(SpriteKind.HealerSlime, 0, function (sprite) {
    MoveCoords = [mySprite.x + randint(-32, 32), mySprite.y + randint(-32, 32)]
    while (Math.abs(MoveCoords[0]) < 16) {
        MoveCoords[0] = mySprite.x + randint(-32, 32)
    }
    while (Math.abs(MoveCoords[1]) < 16) {
        MoveCoords[1] = mySprite.y + randint(-32, 32)
    }
    spriteutils.moveTo(sprite, spriteutils.pos(MoveCoords[0], MoveCoords[1]), 250)
})
