function ClearLevel () {
    for (let value of sprites.allOfKind(SpriteKind.Enemy)) {
        sprites.destroy(value)
    }
    for (let value of sprites.allOfKind(SpriteKind.BSProj)) {
        sprites.destroy(value)
    }
    for (let value of sprites.allOfKind(SpriteKind.Projectile)) {
        sprites.destroy(value)
    }
    for (let value of sprites.allOfKind(SpriteKind.Hole)) {
        sprites.destroy(value)
    }
    for (let value of sprites.allOfKind(SpriteKind.Conjurer)) {
        sprites.destroy(value)
    }
    if (isPlayerSpawned == 0) {
        for (let value of sprites.allOfKind(SpriteKind.Player)) {
            sprites.destroy(value)
        }
    }
    if (Dead == 1) {
        for (let value of sprites.allOfKind(SpriteKind.Player)) {
            sprites.destroy(value)
        }
    }
    if (Level == 5) {
        for (let value of sprites.allOfKind(SpriteKind.Player)) {
            sprites.destroy(value)
        }
        sprites.destroyAllSpritesOfKind(SpriteKind.Projectile)
        sprites.destroyAllSpritesOfKind(SpriteKind.Enemy)
        sprites.destroyAllSpritesOfKind(SpriteKind.StatusBar)
        sprites.destroyAllSpritesOfKind(SpriteKind.HealerSlime)
        sprites.destroyAllSpritesOfKind(SpriteKind.ShieldSLime)
        sprites.destroyAllSpritesOfKind(SpriteKind.BoomSlime)
        sprites.destroyAllSpritesOfKind(SpriteKind.BSProj)
        sprites.destroyAllSpritesOfKind(SpriteKind.Magislime)
        sprites.destroyAllSpritesOfKind(SpriteKind.Conjurer)
    }
    music.stopAllSounds()
    levelCreator()
}
